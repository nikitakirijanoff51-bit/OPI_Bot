def set_webhook():
    if not BOT_TOKEN or not RENDER_EXTERNAL_URL:
        print("‚ùå BOT_TOKEN –∏–ª–∏ RENDER_EXTERNAL_URL –Ω–µ –∑–∞–¥–∞–Ω—ã!")
        return
    webhook_url = f"{RENDER_EXTERNAL_URL}/webhook"
